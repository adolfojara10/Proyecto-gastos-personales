    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:

        String nombre = txtNombre.getText();
        String tipo = (String) cbxTipo.getSelectedItem();

        if (nombre.isBlank() || tipo.equalsIgnoreCase("--Seleccione--")) {
            JOptionPane.showMessageDialog(this, "Llene todo", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            Categoria cat = new Categoria(tipo, Integer.valueOf(txtCodigo.getText()), nombre);

            if (controladorCategoria.create(cat)) {
                JOptionPane.showMessageDialog(this, "Creado con exito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Error", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:

        String nombre = txtNombre.getText();

        if (nombre.isBlank()) {
            JOptionPane.showMessageDialog(this, "Escribe el nombre", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            categoria.setNombre(nombre);

            if (controladorCategoria.update(categoria)) {
                JOptionPane.showMessageDialog(this, "Actualizado con exito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Error", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        if (controladorCategoria.delete(categoria)) {
            JOptionPane.showMessageDialog(this, "Eliminado con exito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "Error", "Aviso", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoriasMouseClicked
        // TODO add your handling code here:

        int fila = tblCategorias.getSelectedRow();

        int id = (int) tblCategorias.getValueAt(fila, 0);
        categoria = controladorCategoria.read(id);

        txtCodigo.setText(String.valueOf(id));
        txtNombre.setText(categoria.getNombre());
        cbxTipo.setSelectedItem(categoria.getTipo());

        btnCrear.setEnabled(false);
        btnActualizar.setEnabled(true);
        btnEliminar.setEnabled(true);


    }//GEN-LAST:event_tblCategoriasMouseClicked

