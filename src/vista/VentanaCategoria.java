    public void llenarTbl() {

        List<Categoria> categorias = controladorCategoria.findAll();

        DefaultTableModel modelo = (DefaultTableModel) tblCategorias.getModel();
        modelo.setRowCount(0);

        for (Categoria cat : categorias) {
            Object[] row = {cat.getId(), cat.getNombre(), cat.getTipo()};
            modelo.addRow(row);
        }

        tblCategorias.setModel(modelo);

    }

