    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:

        int codigo = Integer.valueOf(txtCodigo.getText());
        String categoria = (String) cbxCategoria.getSelectedItem();
        String descripcion = txtAreaDescripcion.getText();
        String dineroAhorroString = txtDinero.getText();
        Date fecha = jDateChooser1.getDate();
        
        if (dineroAhorroString.contains(",")) {
            dineroAhorroString = dineroAhorroString.replaceAll(",", ".");
        }
        //dineroGastadoString = String.format(".2f", dineroGastadoString);
        double dineroAhorrado = Double.valueOf(dineroAhorroString);
        
        if (categoria.equals("--Seleccione--") || dineroAhorroString.isBlank()) {
            JOptionPane.showMessageDialog(this, "Faltan datos", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            BigDecimal ahorroDinero = BigDecimal.valueOf(dineroAhorrado);
            var category = controladorCategoria.findByNombre(categoria);
            
            Ahorro ah = new Ahorro(descripcion, category, fecha,
                    Integer.valueOf(txtCodigo.getText()), ahorroDinero);
            
            if (controladorAhorro.create(ah) && controladorBalance.ahorro(ahorroDinero)) {
                JOptionPane.showMessageDialog(this, "Creado con exito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Error", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
            
        }

    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:

        String categoria = (String) cbxCategoria.getSelectedItem();
        String descripcion = txtAreaDescripcion.getText();
        String dineroAhorroString = txtDinero.getText();
        Date fecha = jDateChooser1.getDate();
        
        if (dineroAhorroString.contains(",")) {
            dineroAhorroString = dineroAhorroString.replaceAll(",", ".");
        }
        //dineroGastadoString = String.format(".2f", dineroGastadoString);
        double dineroAhorrado = Double.valueOf(dineroAhorroString);
        
        if (categoria.equals("--Seleccione--") || dineroAhorroString.isBlank()) {
            JOptionPane.showMessageDialog(this, "Faltan datos", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            BigDecimal ahorroDinero = BigDecimal.valueOf(dineroAhorrado);
            
            var category = controladorCategoria.findByNombre(categoria);
            
            ahorro.setFecha(fecha);
            ahorro.setCategoriaFk(category);
            ahorro.setDineroEntrada(ahorroDinero);
            ahorro.setDescripcion(descripcion);
            
            if (controladorAhorro.update(ahorro) && controladorBalance.gasto(actualizacionAhorro)
                    && controladorBalance.ahorro(ahorroDinero)) {
                JOptionPane.showMessageDialog(this, "Actualizado con exito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Error", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
            
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        int codigo = Integer.valueOf(txtCodigo.getText());
        
        ahorro = controladorAhorro.read(codigo);
        
        if (controladorAhorro.delete(ahorro) && controladorBalance.gasto(ahorro.getDineroEntrada())) {
            JOptionPane.showMessageDialog(this, "Eliminado con exito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "Error", "Aviso", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblAhorroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAhorroMouseClicked
        // TODO add your handling code here:

        int fila = tblAhorro.getSelectedRow();
        
        int codigo = (int) tblAhorro.getValueAt(fila, 0);
        
        ahorro = controladorAhorro.read(codigo);
        
        txtCodigo.setText(String.valueOf(ahorro.getId()));
        cbxCategoria.setSelectedItem(ahorro.getCategoriaFk().getNombre());
        txtDinero.setText(ahorro.getDineroEntrada().toString());
        jDateChooser1.setDate(ahorro.getFecha());
        txtAreaDescripcion.setText(ahorro.getDescripcion());
        
        actualizacionAhorro = ahorro.getDineroEntrada();
        
        btnCrear.setEnabled(false);
        btnActualizar.setEnabled(true);
        btnEliminar.setEnabled(true);

    }//GEN-LAST:event_tblAhorroMouseClicked

