    public void cargarCodigo() {
        txtCodigo.setText(String.valueOf(controladorAhorro.codigo()));
    }
    
    public void cargarCbxAhorro() {
        cbxCategoria.removeAllItems();
        cbxCategoria.addItem("--Seleccione--");
        controladorCategoria.findByTipo("AHORRO").forEach(cat -> {
            cbxCategoria.addItem(cat.getNombre());
        });
        
    }
    
    public void llenarTbl() {
        var ahorros = controladorAhorro.findAll();
        
        DefaultTableModel modelo = (DefaultTableModel) tblAhorro.getModel();
        modelo.setRowCount(0);
        
        for (Ahorro ah : ahorros) {
            Object[] row = {ah.getId(), ah.getCategoriaFk().getNombre(), ah.getDineroEntrada(), 
                ah.getFecha(), ah.getDescripcion()};
            modelo.addRow(row);
        }
        
        tblAhorro.setModel(modelo);
        
    }
    
    public void cargarFecha() {
        jDateChooser1.setDate(new Date());
    }
    
    public void limpiar() {
        cargarCbxAhorro();
        cargarCodigo();
        llenarTbl();
        cargarFecha();
        
        txtAreaDescripcion.setText("");
        cbxCategoria.setSelectedItem(0);
        txtDinero.setText("");
        
        btnCrear.setEnabled(true);
        btnActualizar.setEnabled(false);
        btnEliminar.setEnabled(false);
        
    }

