    private void cbxFiltradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxFiltradoActionPerformed
        // TODO add your handling code here:

        seleccion = (String) cbxFiltrado.getSelectedItem();

        if (seleccion.equalsIgnoreCase("--Seleccione--")) {
            panelCategoria.setEnabled(false);
            panelFechas.setEnabled(false);
            cbxCategoria.setEnabled(false);
            dateChooserInicio.setEnabled(false);
            dateChooserFin.setEnabled(false);
            btnBuscar.setEnabled(false);
        } else if (seleccion.equalsIgnoreCase("Fecha")) {
            panelFechas.setEnabled(true);
            btnBuscar.setEnabled(true);
            cbxCategoria.setEnabled(false);
            dateChooserInicio.setEnabled(true);
            dateChooserFin.setEnabled(true);
        } else if (seleccion.equalsIgnoreCase("Categoria")) {
            panelCategoria.setEnabled(true);
            btnBuscar.setEnabled(true);
            cbxCategoria.setEnabled(true);
            dateChooserInicio.setEnabled(false);
            dateChooserFin.setEnabled(false);
        } else {

            panelCategoria.setEnabled(true);
            panelFechas.setEnabled(true);
            btnBuscar.setEnabled(true);
            cbxCategoria.setEnabled(true);
            dateChooserInicio.setEnabled(true);
            dateChooserFin.setEnabled(true);
        }


    }//GEN-LAST:event_cbxFiltradoActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        if (seleccion.equalsIgnoreCase("Fecha")) {
            listaGastos = controladorGastos.findByFecha(dateChooserInicio.getDate(), dateChooserFin.getDate());
            llenarTblOpcion((ArrayList<Gasto>) listaGastos);
        } else if (seleccion.equalsIgnoreCase("Categoria")) {
            String nombreCategoria = (String) cbxCategoria.getSelectedItem();
            listaGastos = controladorGastos.findByCategoria(nombreCategoria);
            llenarTblOpcion((ArrayList<Gasto>) listaGastos);
        } else {
            String nombreCategoria = (String) cbxCategoria.getSelectedItem();
            listaGastos = controladorGastos.findByFechaCategoria(dateChooserInicio.getDate(),
                    dateChooserFin.getDate(), controladorGastos.findByCategoria(nombreCategoria));
            llenarTblOpcion((ArrayList<Gasto>) listaGastos);
        }

        btnCalcular.setEnabled(true);

    }//GEN-LAST:event_btnBuscarActionPerformed

