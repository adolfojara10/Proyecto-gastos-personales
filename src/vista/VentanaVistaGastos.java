    public void llenarCbxCategoria() {
        var lista = controladorCategoria.findAll();
        cbxCategoria.removeAllItems();
        cbxCategoria.addItem("--Seleccione--");
        lista.forEach(cat -> {
            cbxCategoria.addItem(cat.getNombre());
        });
    }

    public void llenarTbl() {
        var gastos = controladorGastos.findAll();

        DefaultTableModel modelo = (DefaultTableModel) tblGastos.getModel();
        modelo.setRowCount(0);

        for (Gasto gas : gastos) {
            Object[] row = {gas.getId(), gas.getCategoriaFk().getNombre(), gas.getDineroGastado().toString(),
                gas.getFecha().toString(), gas.getDescripcion()};
            modelo.addRow(row);
        }

        tblGastos.setModel(modelo);

    }

    public void llenarTblOpcion(ArrayList<Gasto> gastosResultado) {

        DefaultTableModel modelo = (DefaultTableModel) tblGastos.getModel();
        modelo.setRowCount(0);

        for (Gasto gas : gastosResultado) {
            Object[] row = {gas.getId(), gas.getCategoriaFk().getNombre(), gas.getDineroGastado().toString(),
                gas.getFecha().toString(), gas.getDescripcion()};
            modelo.addRow(row);
        }

        tblGastos.setModel(modelo);

    }

    public void limpiar() {
        llenarTbl();
        llenarCbxCategoria();

        dateChooserFin.setDate(new Date());
        dateChooserInicio.setDate(new Date());
        cbxCategoria.setSelectedIndex(0);
        cbxFiltrado.setSelectedIndex(0);

        cbxCategoria.setEnabled(false);
        dateChooserInicio.setEnabled(false);
        dateChooserFin.setEnabled(false);

        txtTotal.setText("");
        btnCalcular.setEnabled(false);
    }

