    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:

        double total = 0;
        total = listaGastos.stream().map(gas -> Double.valueOf(gas.getDineroGastado().toString())).reduce(total, (accumulator, _item) -> accumulator + _item);

        BigDecimal totalFinal = BigDecimal.valueOf(total).setScale(2, RoundingMode.HALF_UP);

        txtTotal.setText(totalFinal.toString());

    }//GEN-LAST:event_btnCalcularActionPerformed


